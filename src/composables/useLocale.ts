export function useLocale(): { locale: typeof locale; t: (key: string) => string; setLocale: (lang: Locale) => void } {
  const t = (key: string): string => messages[locale.value]?.[key] ?? key
  const setLocale = (lang: Locale): void => { locale.value = lang }
  return { locale, t, setLocale }
}
